# Prompting the user to enter the coefficients of a linear equation ax + b = c
a = int(input("Enter the coefficient 'a': "))
b = int(input("Enter the coefficient 'b': "))
c = int(input("Enter the value of 'c': "))

# Calculating the value of x using the linear equation formula: (c - b) / a
x = (c - b) / a

# Printing the value of x, which is the solution to the linear equation
print("The solution for x is:", x)

 







       


        
        

